# Reescrita de while para for

"""
Código com while:

i = 0

while i <= 10:
    i += 1 
    print(i)
"""

# Código com for
for i in range(11):
    print(i)

# A versão com for fica mais legível
# porque já define início, meio e fim

# O while é mais adequado quando não
# se sabe a quantidade de vezes que
# uma ação será repetida